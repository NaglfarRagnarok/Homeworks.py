"""
ДЗ 8.1 — Додати 1 до числа
Завдання: є список цифр, які разом утворюють число. Треба додати 1
і повернути результат знову як список цифр.

Приклади:
[1, 2, 3, 4] -> [1, 2, 3, 5]
[9, 9, 9]    -> [1, 0, 0, 0]
"""

from typing import List, Optional


def add_one(digits: List[int]) -> List[int]:
    """
    Приймає список цифр (0–9), які представляють число без знаку,
    додає 1 і повертає новий список цифр.

    :param digits: Список цілих від 0 до 9, напр. [1, 2, 3]
    :return: Новий список цифр після додавання одиниці
    """
    # Перевірка вхідних даних (не обовʼязково, але корисно)
    if not digits:
        return [1]
    if any((not isinstance(d, int)) or d < 0 or d > 9 for d in digits):
        raise ValueError("Усі елементи мають бути цілими цифрами 0–9.")

    # 1) Склеїмо цифри у рядок, перетворимо на число, додамо 1
    number = int("".join(str(d) for d in digits)) + 1

    # 2) Розібʼємо назад на список цифр і повернемо результат
    return [int(ch) for ch in str(number)]


def _run_tests() -> None:
    """Набір базових перевірок із завдання."""
    assert add_one([1, 2, 3, 4]) == [1, 2, 3, 5], "Test1"
    assert add_one([9, 9, 9]) == [1, 0, 0, 0], "Test2"
    assert add_one([0]) == [1], "Test3"
    assert add_one([9]) == [1, 0], "Test4"
    print("ОК ✅")


if __name__ == "__main__":
    _run_tests()


